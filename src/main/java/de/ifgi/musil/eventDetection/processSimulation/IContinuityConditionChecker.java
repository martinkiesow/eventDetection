package de.ifgi.musil.eventDetection.processSimulation;

import org.joda.time.Duration;

/**
 * Continuity condition checkers define the conditions for all spatial, temporal
 * and other measured properties of an {@link Event event's} {@link AFocus foci}
 * .
 * 
 * @author Kiesow
 * 
 */
public interface IContinuityConditionChecker {

	public boolean checkHomogenity(AFocus target) throws Exception;

	public double getXRange();

	public double getYRange();

	public Duration getTemporalRange();

}
